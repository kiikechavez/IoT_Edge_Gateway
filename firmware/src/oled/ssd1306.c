#define FONT_FIRST 0x20
#define FONT_LAST  0x5A

/* -------------------------------------------------------------------------
 *  Primitivas I2C
 * -----------------------------------------------------------------------*/
static void ssd1306_cmd(uint8_t c) {
    uint8_t buf[2] = {0x00, c};
    i2c_write_blocking(I2C_OLED, OLED_ADDR, buf, 2, false);
}

/* Posiciona el cursor en modo page-addressing */
static void oled_set_cursor(uint8_t page, uint8_t col) {
    ssd1306_cmd(0xB0 | (page & 0x07));
    ssd1306_cmd(0x00 | (col & 0x0F));
    ssd1306_cmd(0x10 | (col >> 4));
}

/* Borra toda la pantalla (escribe 0x00 en todas las paginas) */
static void oled_clear_all(void) {
    uint8_t buf[129];
    buf[0] = 0x40;
    memset(buf + 1, 0x00, 128);
    for (uint8_t p = 0; p < 8; p++) {
        oled_set_cursor(p, 0);
        i2c_write_blocking(I2C_OLED, OLED_ADDR, buf, 129, false);
    }
}

/* Escribe un caracter (6 bytes: 5 datos + 1 gap) en la posicion actual */
static void oled_write_char(uint8_t c) {
    if (c >= 'a' && c <= 'z') c -= 0x20;   /* minuscula -> mayuscula */
    if (c < FONT_FIRST || c > FONT_LAST) c = '?';
    uint8_t buf[7];
    buf[0] = 0x40;
    const uint8_t *g = font5x7[c - FONT_FIRST];
    buf[1] = g[0]; buf[2] = g[1]; buf[3] = g[2];
    buf[4] = g[3]; buf[5] = g[4];
    buf[6] = 0x00;  /* gap entre caracteres */
    i2c_write_blocking(I2C_OLED, OLED_ADDR, buf, 7, false);
}

/* Escribe una cadena a partir de (page, col) */
static void oled_write_str(uint8_t page, uint8_t col, const char *s) {
    oled_set_cursor(page, col);
    while (*s) oled_write_char((uint8_t)*s++);
}

/* -------------------------------------------------------------------------
 *  API publica
 * -----------------------------------------------------------------------*/
void oled_init(void) {
    i2c_init(I2C_OLED, I2C_OLED_FREQ);
    gpio_set_function(PIN_I2C_SDA, GPIO_FUNC_I2C);
    gpio_set_function(PIN_I2C_SCL, GPIO_FUNC_I2C);
    gpio_pull_up(PIN_I2C_SDA);
    gpio_pull_up(PIN_I2C_SCL);

    static const uint8_t init_seq[] = {
        0xAE,       /* display off               */
        0xD5, 0x80, /* clock divide ratio        */
        0xA8, 0x3F, /* multiplex ratio 64        */
        0xD3, 0x00, /* display offset 0          */
        0x40,       /* display start line 0      */
        0x8D, 0x14, /* charge pump on            */
        0x20, 0x02, /* PAGE addressing mode      */
        0xA1,       /* segment remap             */
        0xC8,       /* COM output scan direction */
        0xDA, 0x12, /* COM pins HW config        */
        0x81, 0xCF, /* contrast                  */
        0xD9, 0xF1, /* pre-charge period         */
        0xDB, 0x40, /* VCOMH deselect level      */
        0xA4,       /* display follows RAM       */
        0xA6,       /* normal (non-inverted)     */
        0xAF        /* display on                */
    };
    for (size_t i = 0; i < sizeof(init_seq); i++) ssd1306_cmd(init_seq[i]);

    oled_clear_all();
}

void oled_service(const telemetry_t *t) {
    char line[22];

    /* Linea 0 — estado WiFi + IP */
    snprintf(line, sizeof(line), "WIFI:%-3s %s",
             t->wifi_connected ? "ON" : "OFF",
             t->ip_addr[0] ? t->ip_addr : "---");
    oled_write_str(0, 0, line);

    /* Linea 1 — temperatura + duty PWM */
    snprintf(line, sizeof(line), "T:%.1fC  D:%u%%",
             (double)t->temp_internal,
             (unsigned)(t->pwm_duty * 100u / 255u));
    oled_write_str(1, 0, line);

    /* Linea 2 — registros Modbus */
    snprintf(line, sizeof(line), "R0:%u R1:%u",
             t->modbus_regs[0], t->modbus_regs[1]);
    oled_write_str(2, 0, line);

    /* Linea 3 — contadores */
    snprintf(line, sizeof(line), "ERR:%u UPL:%lu",
             t->modbus_errors, (unsigned long)t->total_uploads);
    oled_write_str(3, 0, line);

    printf("[OLED] WiFi=%s  IP=%s  T=%.1fC  Duty=%u  Errs=%u\n",
           t->wifi_connected ? "ON" : "OFF",
           t->ip_addr[0] ? t->ip_addr : "---",
           (double)t->temp_internal, t->pwm_duty, t->modbus_errors);
}
